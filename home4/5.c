#include <stdio.h>

int main() {
    int x1, y1, x2, y2;

    // Ввод координат двух точек
    scanf("%d %d %d %d", &x1, &y1, &x2, &y2);

    // Проверка на вертикальную прямую (X1 == X2)
    if (x1 == x2) {
        printf("Ошибка: вертикальная прямая, коэффициент k неопределен\n");
        return 1;
    }

    // Вычисление углового коэффициента k
    double k = (double)(y2 - y1) / (x2 - x1);

    // Вычисление свободного члена b
    double b = y1 - k * x1;

    // Выводим k и b с точностью до двух знаков после запятой
    printf("%.2f %.2f\n", k, b);

    return 0;
}

