#include <stdio.h>

// Функция для проверки, что все цифры числа идут по возрастанию
int grow_up(int n) {
    int prev_digit = 10;  // Инициализация предыдущей цифры значением, больше любого возможного (цифра от 0 до 9)

    // Пока число больше 0, извлекаем цифры
    while (n > 0) {
        int current_digit = n % 10;  // Получаем последнюю цифру
        n /= 10;  // Убираем последнюю цифру

        // Если текущая цифра меньше или равна предыдущей, то цифры не по возрастанию
        if (current_digit >= prev_digit) {
            return 0;  // Возвращаем 0 (NO)
        }

        prev_digit = current_digit;  // Обновляем предыдущую цифру
    }

    return 1;  // Если все цифры идут по возрастанию
}

int main() {
    int number;

    // Ввод числа
    scanf("%d", &number);

    // Проверка и вывод результата
    if (grow_up(number)) {
        printf("YES\n");
    } else {
        printf("NO\n");
    }

    return 0;
}

