#include <stdio.h>

int main() {
    char c;
    int stack = 0;  // Счётчик открывающих скобок

    // Чтение строки до точки
    while ((c = getchar()) != '.') {
        if (c == '(') {
            stack++;  // Увеличиваем счётчик при встрече '('
        } else if (c == ')') {
            stack--;  // Уменьшаем счётчик при встрече ')'
        }

        // Если на каком-то этапе количество закрывающих скобок больше открывающих, строка некорректна
        if (stack < 0) {
            printf("NO\n");
            return 0;
        }
    }

    // Если после обработки всей строки счётчик скобок равен нулю, то скобки расставлены корректно
    if (stack == 0) {
        printf("YES\n");
    } else {
        printf("NO\n");
    }

    return 0;
}

