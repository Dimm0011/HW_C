#include <stdio.h>

// Рекурсивная функция для нахождения максимального значения
int max_find(int max) {
    int n;
    scanf("%d", &n);  // Считываем очередное число

    // Если число равно 0, возвращаем максимальное значение
    if (n == 0) {
        return max;
    }

    // Обновляем максимальное значение, если текущее число больше
    if (n > max) {
        max = n;
    }

    // Рекурсивный вызов для следующего числа
    return max_find(max);
}

int main() {
    // Начинаем поиск с минимального возможного значения (например, минимальное число -100000)
    int result = max_find(-100000);

    // Выводим найденное максимальное число
    printf("%d\n", result);
    return 0;
}

