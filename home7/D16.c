#include <stdio.h>

// Рекурсивная функция для проверки, является ли число точной степенью двойки
int is2pow(int n) {
    if (n <= 0) {
        return 0;  // Число должно быть натуральным, а не отрицательным или нулем
    }
    if (n == 1) {
        return 1;  // 1 - это точная степень двойки (2^0 = 1)
    }
    if (n % 2 != 0) {
        return 0;  // Если число нечетное и больше 1, то это не степень двойки
    }
    return is2pow(n / 2);  // Рекурсивно делим на 2
}

int main() {
    int n;

    // Ввод числа
    scanf("%d", &n);

    // Вывод результата
    if (is2pow(n)) {
        printf("YES\n");
    } else {
        printf("NO\n");
    }

    return 0;
}

