#include <stdio.h>

// Рекурсивная функция для печати числа в двоичной системе счисления
void print_binary(int num) {
    // Базовый случай: если число равно 0, ничего не выводим
    if (num == 0) {
        return;
    }

    // Рекурсивно вызываем функцию для числа, делённого на 2
    print_binary(num / 2);

    // Печатаем остаток от деления на 2 (0 или 1)
    printf("%d", num % 2);
}

int main() {
    int num;
    // Ввод числа
    scanf("%d", &num);

    // Особый случай для нуля
    if (num == 0) {
        printf("0");
    } else {
        // Вызываем рекурсивную функцию для печати числа в двоичной системе
        print_binary(num);
    }

    printf("\n");  // Печатаем перевод строки
    return 0;
}

