#include <stdio.h>

// Рекурсивная функция для печати чисел от N до 1
void print_numbers(int n) {
    // Базовый случай: если n меньше 1, завершаем рекурсию
    if (n < 1) {
        return;
    }

    // Печатаем текущее число
    printf("%d ", n);

    // Рекурсивно вызываем функцию для следующего числа
    print_numbers(n - 1);
}

int main() {
    int n;

    // Вводим число N
    scanf("%d", &n);

    // Вызываем рекурсивную функцию для печати чисел от N до 1
    print_numbers(n);

    return 0;
}
