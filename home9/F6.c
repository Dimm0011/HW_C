#include <stdio.h>

int is_two_same(int size, int a[]) {
    // Используем множество для отслеживания уникальных элементов
    int seen[100001] = {0};  // Массив, чтобы запомнить, встречался ли элемент

    for (int i = 0; i < size; i++) {
        if (seen[a[i]] == 1) {  // Если элемент уже встречался
            return 1;  // Нашли одинаковые элементы
        }
        seen[a[i]] = 1;  // Запоминаем текущий элемент
    }

    return 0;  // Нет одинаковых элементов
}

