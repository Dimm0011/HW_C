#include <stdio.h>

// Функция для сжатия последовательностей одинаковых элементов
int compression(int a[], int b[], int N) {
    int count = 1;  // Счётчик для текущей серии
    int j = 0;      // Индекс для массива b[]

    // Обрабатываем первую серию, если она состоит из единиц
    if (a[0] == 1) {
        b[j++] = 0;  // Если первый элемент 1, записываем 0 в b[]
    }

    // Итерируем по массиву a[], начиная с 1-го элемента
    for (int i = 1; i < N; i++) {
        if (a[i] == a[i - 1]) {
            count++;  // Увеличиваем счётчик, если элементы одинаковые
        } else {
            b[j++] = count;  // Добавляем длину серии в массив b[]
            count = 1;        // Сбрасываем счётчик для новой серии
        }
    }

    // Добавляем последнюю серию
    b[j++] = count;

    return j;  // Возвращаем количество серий
}
